# Comparison operators: symbols we use in python to compare two values
# comparison operators help you ask questions, the result should always translate to true or false

# Equal to (==)
#one equal to(=) is used as an assignment
#double equal to(==) is used for comparison

print("Is 5 equal to 5?",5 == 5)

#Not equal to (!=)
print("Is 5 not equal to 5?",5 != 5)

# Greater than (>)
print ("Is 7 greater than 10 ?",7>10)

# Less than (<)
print ("Is 7 less than 10 ?",7<10)

# Greater than or Equal to(>=)
print ("Is 7 greater than or equal to 10 ?",7>=10)

# Less than  or Equal to(>=)
print ("Is 7 less than or equal to 10 ?",7<=10)